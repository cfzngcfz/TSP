function [S,R]=TSP_Metropolis(S1,S2,Dist,T)
% 输入
% S1：  当前解
% S2:   新解
% D:    距离矩阵（两两城市的之间的距离）
% T:    当前温度
% 输出
% S：   下一个当前解
% R：   下一个当前解的路线距离
R1=ObjF(S1,Dist);           %计算路线长度
R2=ObjF(S2,Dist);           %计算路线长度
if R2<R1                    %如果能力降低 接受新路线
    S=S2;
    R=R2;
elseif exp((R1-R2)/T)>=rand	%以exp(-dC/T)概率接受新路线
    S=S2;
    R=R2;
else                        %不接受新路线
    S=S1;
    R=R1;
end
end